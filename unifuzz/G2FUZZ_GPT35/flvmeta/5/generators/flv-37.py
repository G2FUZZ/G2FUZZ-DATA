import os

# Create a directory for storing FLV files
os.makedirs('./tmp/', exist_ok=True)

# Generate FLV file with various data types
header = b'FLV\x01\x01\x00\x00\x00\x09\x00\x00\x00\x00\x00\x00\x00\x00'
audio_data = b'\x00\x00\x00\x13\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00'
video_data = b'\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00'
script_data = b'\x00\x00\x00\x06\x00\x00\x00\x0f\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00'
cue_points = b'\x00\x00\x00\x07\x00\x00\x00\x10\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00'

with open('./tmp/sample_complex.flv', 'wb') as f:
    f.write(header)
    f.write(audio_data)
    f.write(video_data)
    f.write(script_data)
    f.write(cue_points)

print('FLV file with complex file structures generated successfully.')